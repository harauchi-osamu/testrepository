COMMENT ON COLUMN dbctr9999.IMG_PARAM.REDUCE_RATE IS '縮小率';
COMMENT ON COLUMN dbctr9999.IMG_PARAM.IMG_TOP IS 'TOP基点';
COMMENT ON COLUMN dbctr9999.IMG_PARAM.IMG_LEFT IS 'LEFT基点';
COMMENT ON COLUMN dbctr9999.IMG_PARAM.IMG_WIDTH IS '幅';
COMMENT ON COLUMN dbctr9999.IMG_PARAM.IMG_HEIGHT IS '高さ';
COMMENT ON COLUMN dbctr9999.IMG_PARAM.IMG_BASE_POINT IS 'イメージ内基点';
COMMENT ON COLUMN dbctr9999.IMG_PARAM.XSCROLL_LEFT IS '水平スクロール-左基準';
COMMENT ON COLUMN dbctr9999.IMG_PARAM.XSCROLL_VALUE IS '水平スクロール-スクロール位置';
COMMENT ON COLUMN dbctr9999.IMG_PARAM.XSCROLL_RIGHT IS '右基準位置';
exit;