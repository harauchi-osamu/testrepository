DROP TABLE dbctr9999.IMG_PARAM;
CREATE TABLE dbctr9999.IMG_PARAM (
    GYM_ID NUMBER(3,0) NOT NULL,
    DSP_ID NUMBER(3,0) NOT NULL,
    IMG_FILE VARCHAR2(30),
    REDUCE_RATE NUMBER(4,1) default 0  NOT NULL,
    IMG_TOP NUMBER(10,1) default -1  NOT NULL,
    IMG_LEFT NUMBER(10,1) default -1  NOT NULL,
    IMG_WIDTH NUMBER(10,1) default -1  NOT NULL,
    IMG_HEIGHT NUMBER(10,1) default -1  NOT NULL,
    IMG_BASE_POINT NUMBER(10,1) default -1  NOT NULL,
    XSCROLL_LEFT NUMBER(10,1) default 0  NOT NULL,
    XSCROLL_VALUE NUMBER(4,0) default 0  NOT NULL,
    XSCROLL_RIGHT NUMBER(10,1) default 0  NOT NULL,
CONSTRAINT PK_DBCTR9999_IMG_PARAM PRIMARY KEY (
     GYM_ID
    ,DSP_ID
));
