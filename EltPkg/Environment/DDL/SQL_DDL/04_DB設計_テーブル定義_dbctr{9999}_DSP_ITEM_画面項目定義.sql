COMMENT ON COLUMN dbctr9999.DSP_ITEM.ITEM_TYPE IS '項目タイプ';
COMMENT ON COLUMN dbctr9999.DSP_ITEM.ITEM_LEN IS '項目文字長';
COMMENT ON COLUMN dbctr9999.DSP_ITEM.POS IS '外部連携ファイル項目位置:バイナリデータ開始位置';
COMMENT ON COLUMN dbctr9999.DSP_ITEM.DUP IS 'DUP有無:0：DUP不可 1：DUP可';
