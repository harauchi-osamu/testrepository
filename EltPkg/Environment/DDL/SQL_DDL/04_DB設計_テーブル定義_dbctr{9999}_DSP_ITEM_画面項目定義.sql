DROP TABLE dbctr9999.DSP_ITEM;
CREATE TABLE dbctr9999.DSP_ITEM (
    GYM_ID NUMBER(3,0) NOT NULL,
    DSP_ID NUMBER(3,0) NOT NULL,
    ITEM_ID NUMBER(3,0) NOT NULL,
    ITEM_DISPNAME VARCHAR2(40),
    ITEM_TYPE VARCHAR2(1),
    ITEM_LEN NUMBER(2,0) default 0  NOT NULL,
    POS NUMBER(4,0) default 0  NOT NULL,
    DUP VARCHAR2(1),
    AUTO_INPUT VARCHAR2(1),
    ITEM_SUBRTN VARCHAR2(150),
    BLANK_FLG VARCHAR2(1),
CONSTRAINT PK_DBCTR9999_DSP_ITEM PRIMARY KEY (
     GYM_ID
    ,DSP_ID
    ,ITEM_ID
));
